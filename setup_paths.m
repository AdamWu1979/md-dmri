    'models/vasco16',...
    'models/fexi11',...
    'models/quick_dti',...
    'models/dti_euler',...
    'models/axsym_pa',...
    };


t = fileparts(mfilename('fullpath'));

for c_package = 1:numel(packages_dir)
    addpath(fullfile(t, packages_dir{c_package}), '-end');
end

disp (char(10))
disp 'Done configuring paths for framework!'

