function setup_paths()
% function setup_paths()
%
% Restores the default paths and adds all relevant subdirs to the
% path. Run this when you start MATLAB to use the code.

clc;
disp('Welcome!');
disp('Restoring default path');
restoredefaultpath;

packages_dir = {...
    'acq/dtd', ...
    'acq/qmas', ...
    'acq/uvec', ...
    'man', ...
    'mdm', ...
    'mio', ...
    'mio/elastix', ...
